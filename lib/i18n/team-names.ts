export function translateTeamNameToHebrew(name: string | null | undefined) {
  const value = String(name ?? "").trim();
  if (!value) return "";
  if (/[\u0590-\u05ff]/.test(value)) return value;

  return TEAM_NAME_HE_BY_KEY[normalizeTeamNameKey(value)] ?? value;
}

export function normalizeTeamNameKey(value: string | null | undefined) {
  return String(value ?? "")
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/gi, "")
    .toLowerCase();
}
